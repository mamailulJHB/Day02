void 	ft_is_negative(int n)
{
	while (n >= -2147483648 && n <= 2147483647)
	{
		if (n < 0)
			ft_putchar('N');
		else if (n >= 0 || n == '\0')
			ft_putchar('P');
		else return;
	}
}
